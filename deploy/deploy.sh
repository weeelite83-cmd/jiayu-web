#!/bin/bash

# ============================================================
# 项目部署脚本 - 在服务器上执行
# ============================================================

PROJECT_DIR="/var/www/jiayu-web"

echo "=========================================="
echo "开始部署项目..."
echo "=========================================="

# 进入项目目录
cd $PROJECT_DIR

# 安装依赖
echo "[1/3] 安装依赖..."
pnpm install

# 构建项目
echo "[2/3] 构建项目..."
pnpm run build

# 停止旧进程并启动新进程
echo "[3/3] 启动服务..."
pm2 delete jiayu-web 2>/dev/null || true
pm2 start pnpm --name "jiayu-web" -- start
pm2 save

echo "=========================================="
echo "部署完成！"
echo "访问: http://jayu-stainless.com"
echo "=========================================="
